#menampilkan informasi program
print("Konversi Suhu (Celcius ke Fahrenheit)")

#input suhu dalam Celcius
C = float(input("Masukkan suhu(celcius): "))

#melakukan konversi suhu ke Fahrenheit
F = 9 * (C+32) / 5

#menampilkan hasil konversi
print("Celcius: ", C)
print("Fahrenheit: ", F)

